/**
 * Generate Evaluation Method Primary Fide IDs
 *
 * Calculates the primary Fide IDs (0xe0...) for all evaluation methods.
 * These are Statement-derived primaries used as predicates in evaluation statements.
 *
 * Genesis Statement Pattern:
 * - Subject: Evaluation Method Entity (0xe5... with GitHub URL)
 * - Predicate: schema:name
 * - Object: Method Name (e.g., "Fide-NameResolution-v1")
 *
 * The Statement ID (0x00...) becomes the raw identifier for the primary (0xe0...)
 *
 * Usage: pnpm fcp:e
 */

import { calculateFideId, calculateStatementFideId } from '@/lib/fcp/fide-id';
import {
    METHOD_ALIAS_RESOLUTION_TRUST,
    METHOD_STATEMENT_ACCURACY,
    METHOD_QUALITY_SCORE,
    METHOD_RELIABILITY_SCORE,
    METHOD_ALIGNMENT_INDEX,
    METHOD_SKILL_PROFICIENCY,
    METHOD_BRIDGING_CONSENSUS,
    METHOD_NAME_RESOLUTION,
    METHOD_DESCRIPTION_RESOLUTION,
    PREDICATE_NAME,
} from '@/lib/fcp/vocab';
import { writeFileSync } from 'fs';
import { join } from 'path';

// Map of GitHub URLs to their method names
const EVALUATION_METHODS = new Map<string, string>([
    [METHOD_ALIAS_RESOLUTION_TRUST, 'Fide-AliasResolutionTrust-v1'],
    [METHOD_BRIDGING_CONSENSUS, 'Fide-BridgingConsensus-v1'],
    [METHOD_STATEMENT_ACCURACY, 'Fide-StatementAccuracy-v1'],
    [METHOD_QUALITY_SCORE, 'Fide-QualityScore-v1'],
    [METHOD_RELIABILITY_SCORE, 'Fide-ReliabilityScore-v1'],
    [METHOD_ALIGNMENT_INDEX, 'Fide-AIAlignment-v1'],
    [METHOD_SKILL_PROFICIENCY, 'Fide-SkillProficiency-v1'],
    [METHOD_NAME_RESOLUTION, 'Fide-NameResolution-v1'],
    [METHOD_DESCRIPTION_RESOLUTION, 'Fide-DescriptionResolution-v1'],
]);

/**
 * Calculate the primary Fide ID for an evaluation method
 */
async function calculateMethodPrimaryId(githubUrl: string, methodName: string): Promise<{
    entityId: string;
    genesisStatementId: string;
    primaryId: string;
}> {
    // 1. Calculate entity ID (0xe5... with GitHub URL as raw identifier)
    const entityId = await calculateFideId('EvaluationMethod', 'Product', githubUrl);

    // 2. Calculate genesis statement ID (schema:name statement)
    const predicateId = await calculateFideId('CreativeWork', 'CreativeWork', PREDICATE_NAME);
    const nameObjectId = await calculateFideId('CreativeWork', 'CreativeWork', methodName);
    const genesisStatementId = await calculateStatementFideId(
        entityId as `0x${string}`,
        predicateId as `0x${string}`,
        nameObjectId as `0x${string}`
    );

    // 3. Calculate primary ID (0xe0... with genesis Statement ID as raw identifier)
    const primaryId = await calculateFideId('EvaluationMethod', 'Statement', genesisStatementId);

    return { entityId, genesisStatementId, primaryId };
}

async function generateConstantsFile() {
    const results: Array<{
        name: string;
        url: string;
        methodName: string;
        entityId: string;
        genesisStatementId: string;
        primaryId: string;
    }> = [];

    console.log('🔍 Calculating evaluation method primary IDs...\n');

    for (const [githubUrl, methodName] of EVALUATION_METHODS.entries()) {
        const { entityId, genesisStatementId, primaryId } = await calculateMethodPrimaryId(githubUrl, methodName);

        // Extract constant name from method name
        // "Fide-NameResolution-v1" -> "NAME_RESOLUTION"
        const constantName = methodName
            .replace(/^Fide-/, '') // Remove "Fide-" prefix
            .replace(/-v\d+$/, '') // Remove "-v1" suffix
            .replace(/([a-z])([A-Z])/g, '$1_$2') // camelCase to snake_case
            .replace(/-/g, '_') // kebab-case to snake_case
            .toUpperCase();

        results.push({
            name: constantName,
            url: githubUrl,
            methodName,
            entityId,
            genesisStatementId,
            primaryId,
        });

        console.log(`✓ ${methodName}`);
        console.log(`  Entity (0xe5): ${entityId}`);
        console.log(`  Genesis (0x00): ${genesisStatementId}`);
        console.log(`  Primary (0xe0): ${primaryId}\n`);
    }

    // Generate TypeScript file content
    const lines = [
        '/**',
        ' * Evaluation Method Fide IDs (Primary)',
        ' *',
        ' * Auto-generated by scripts/fcp/generate-evaluation-method-ids.ts',
        ' * DO NOT EDIT MANUALLY - Run `pnpm fcp:e` to regenerate',
        ' *',
        ' * These are the primary Fide IDs (0xe0...) used as predicates in evaluation statements.',
        ' *',
        ' * Genesis Statement Structure:',
        ' * - Subject: 0xe5... (evaluation method entity, rawIdentifier = GitHub URL)',
        ' * - Predicate: 0x66... (schema:name)',
        ' * - Object: 0x66... (method name, e.g., "Fide-NameResolution-v1")',
        ' *',
        ' * The genesis statement ID (0x00...) becomes the raw identifier for the primary (0xe0...).',
        ' */',
        '',
    ];

    for (const result of results) {
        const { name, methodName, primaryId } = result;

        lines.push(`// ${methodName}`);
        lines.push(`export const EM_FIDE_ID_${name} = '${primaryId}' as const;`);
        lines.push('');
    }

    const fileContent = lines.join('\n');

    // Write to file
    const outputPath = join(process.cwd(), 'lib', 'fcp', 'evaluation-methods.ts');
    writeFileSync(outputPath, fileContent, 'utf-8');

    console.log(`✅ Generated ${outputPath}`);
    console.log(`\n📦 Exported ${results.length} primary Fide IDs (EM_FIDE_ID_*)`);
}

// Run the generator
generateConstantsFile().catch((error) => {
    console.error('❌ Failed to generate evaluation method IDs:', error);
    process.exit(1);
});
